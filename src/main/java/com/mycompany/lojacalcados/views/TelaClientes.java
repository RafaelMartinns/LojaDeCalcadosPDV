    public void atualizarTabela(){
    
         ArrayList<Cliente> lista = ClienteDAO.consultar();
        
        DefaultTableModel modeloTabela = (DefaultTableModel) tblClientes.getModel();
        
        modeloTabela.setRowCount(0);
        
        for (Cliente item : lista) {
            SimpleDateFormat formata = new SimpleDateFormat("dd-MM-yyyy");
            if (txtNomeClienteProcurar.getText().isEmpty() || item.getNome().toLowerCase().contains(txtNomeClienteProcurar.getText().toLowerCase())) {
                 modeloTabela.addRow(new String[]{
                String.valueOf(item.getId()),
                item.getNome(),
                item.getCpf(),
               
                formata.format(item.getNascimento()),
                item.getSexo(),
                item.getEstadoCivil(),
                item.getEndereco(),
                item.getEmail(),
                item.getTelefone()
          
                });
            }
           
            
        }
    
    }
    
