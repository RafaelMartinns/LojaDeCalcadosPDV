    private void btnExcluirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirClienteActionPerformed
        int linhaSelecionada = tblClientes.getSelectedRow();
        if (linhaSelecionada>=0) {
            DefaultTableModel modeloClientes =(DefaultTableModel)tblClientes.getModel();
            int idExcluir = Integer.parseInt(String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 0)));
            
            boolean retorno = ClienteDAO.excluir(idExcluir);
            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Cliente excluído com sucesso!");
                atualizarTabela();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha ao excluir cliente");
            }
            
        }
    }//GEN-LAST:event_btnExcluirClienteActionPerformed

    private void btnAlterarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarClienteActionPerformed
        DefaultTableModel modeloClientes = (DefaultTableModel) tblClientes.getModel();
        int linhaSelecionada = tblClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0)
        {
        int id = Integer.parseInt(String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 0)));
        String nome = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 1));
        String cpf = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 2));
        
        String nasc = (String)modeloClientes.getValueAt(linhaSelecionada, 3);
        SimpleDateFormat formata = new SimpleDateFormat("dd-MM-yyyy");
        Date nascimento=null;
            try {
                nascimento = formata.parse(nasc);
            } catch (ParseException ex) {
                Logger.getLogger(TelaClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        String sexo = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 4));
        String estadoCivil = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 5));
        String endereco = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 6));
        String email = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 7));
        String fone = String.valueOf(modeloClientes.getValueAt(linhaSelecionada, 8));
        
        objalterar = new Cliente(nome, cpf, nascimento, endereco, sexo, estadoCivil, email, fone);
        objalterar.setId(id);
        pnlGuiasCliente.setSelectedIndex(0);
        txtIdCliente.setText(String.valueOf(id));
        txtNomeClienteCadastro.setText(String.valueOf(nome));
        txtCpfClienteCadastro.setText(cpf);
        jdcNascimentoClienteCadastro.setDate(nascimento);
        cbxSexoClienteCadastro.setSelectedItem(sexo);
        cbxEstadoCivilClienteCadastro.setSelectedItem(estadoCivil);
        txtEndereçoClienteCadastro.setText(endereco);
        txtEmailClienteCadastro.setText(email);
        txtFoneClienteCadastro.setText(fone);
        
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha da tabela");
        }
    }//GEN-LAST:event_btnAlterarClienteActionPerformed

