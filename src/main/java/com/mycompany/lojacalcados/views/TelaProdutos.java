    public void atualizarTabela(){
         
        ArrayList<Produto> lista = ProdutoDAO.consultar(txtNomeProdutoPesquisar.getText()); 
        
        DefaultTableModel modeloTabela = (DefaultTableModel) tblProdutos.getModel();
        
        modeloTabela.setRowCount(0);
        
        for (Produto item : lista)
        {
            modeloTabela.addRow(new String[]{
                String.valueOf(item.getId()),
                item.getNome(),
                item.getMarca(),                
                item.getSexo(),
                String.valueOf(item.getPreco())
            });
        }
    } 
    
    public void atualizarTabelaTamanhos(int idProduto){
         
        if(idProduto <= 0){
            int linhaSelecionada = tblProdutos.getSelectedRow();
            if(linhaSelecionada >= 0){
                DefaultTableModel modeloTabela = (DefaultTableModel) tblProdutos.getModel();
                idProduto = Integer.parseInt(modeloTabela.getValueAt(linhaSelecionada, 0).toString());
            }else{
                JOptionPane.showMessageDialog(rootPane, "Selecione um produto primeiro");
            }
        }
        
        ArrayList<ProdutoTamanho> lista = ProdutoTamanhoDAO.consultarByProduto(idProduto);

        DefaultTableModel modeloTabelaTamanho = (DefaultTableModel) tblTamanhos.getModel();

        modeloTabelaTamanho.setRowCount(0);

        for (ProdutoTamanho item : lista)
        {
            modeloTabelaTamanho.addRow(new String[]{
                String.valueOf(item.getId()),
                String.valueOf(item.getTamanho()),
                String.valueOf(item.getQuantidade())
            });
        }

       
    } 
        
