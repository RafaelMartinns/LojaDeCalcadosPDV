    private void btnAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoActionPerformed
        try {

            Validador.validarObrigatorio(txtTituloProduto);
            var retornoValidador = Validador.validarValor(txtPrecoProduto);
            
            if(!retornoValidador)
                return;
            
            String nome = txtTituloProduto.getText();
            String sexo = String.valueOf(cbxSexoProdutoCadastro.getSelectedItem());            
            String marca = String.valueOf(cbxMarca.getSelectedItem());
            Double preco = Double.parseDouble(txtPrecoProduto.getText());
            
            
            Produto produto = new Produto(nome, marca, preco, sexo );

            if(txtCodigoProduto.getText().isEmpty())
            {
                int idProdutoGerado = ProdutoDAO.adicionar(produto);
            
                if(idProdutoGerado > 0){
                    JOptionPane.showMessageDialog(rootPane, "O Produto foi cadastrado com sucesso");
                    TelaProdutoTamanho telaProdutoTamanho = new TelaProdutoTamanho();
                    telaProdutoTamanho.setVisible(true);
                    telaProdutoTamanho.setCodigoProdutoAdicionado(String.valueOf(idProdutoGerado));
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Falha ao cadastrar produto");
                }
                
            }else{
                produto.setId(Integer.parseInt(txtCodigoProduto.getText()));
                        
                boolean retorno = ProdutoDAO.alterar(produto);
                
                 if (retorno) 
                     JOptionPane.showMessageDialog(rootPane, "Produto atualizado com sucesso!");
                 else
                     JOptionPane.showMessageDialog(rootPane, "Falha ao atualizar!");
                 
            }
           
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarProdutoActionPerformed

